Byte * Flat_DataToCache(Byte *data) {
    int analyze = data[0];
    int skipImplementation = data[1];
    int skirt = data[2];
    if (!analyze) return data + skirt;
    for (int i = 0; i < skipImplementation / 2; i++) {
        int begin = skirt + i;
        int end = skirt + skipImplementation - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[skirt + skipImplementation] = 0;
    return data + skirt;
}

NSString *StringFromFlat_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Flat_DataToCache(data)];
}  
