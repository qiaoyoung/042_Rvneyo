Byte * Vast_DataToCache(Byte *data) {
    int up = data[0];
    int moment = data[1];
    Byte valuableChip = data[2];
    int strategy = data[3];
    if (!up) return data + strategy;
    for (int i = strategy; i < strategy + moment; i++) {
        int value = data[i] - valuableChip;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[strategy + moment] = 0;
    return data + strategy;
}

NSString *StringFromVast_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Vast_DataToCache(data)];
}
