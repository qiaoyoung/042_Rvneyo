    int split = data[1];
    Byte bound = data[2];
    int upTribe = data[3];
    if (!warm) return data + upTribe;
    for (int i = upTribe; i < upTribe + split; i++) {
        int value = data[i] - bound;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[upTribe + split] = 0;
    return data + upTribe;
}

